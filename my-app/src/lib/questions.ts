export const generateQuestions = async (topic: QuizTopic): Promise<Question[]> => {
  let filteredQuestions = PDF_QUESTIONS;
  
  if (topic === QuizTopic.SIGNS) {
    filteredQuestions = PDF_QUESTIONS.filter(q => q.category.includes('إشارات') || q.category === 'إشارات المرور');
  } else if (topic === QuizTopic.SAFETY) {
    filteredQuestions = PDF_QUESTIONS.filter(q => q.category === 'سلامة' || q.category === 'السرعة');
  } else if (topic === QuizTopic.SCENARIOS) {
      filteredQuestions = PDF_QUESTIONS.filter(q => ['أسبقية', 'شرطي المرور', 'اندماج', 'طريق سيار', 'قواعد السير'].includes(q.category));
  } else if (topic === QuizTopic.FINES) {
     // Currently no fines in dataset, filter specific category if exists or return empty
     filteredQuestions = PDF_QUESTIONS.filter(q => q.category === 'مخالفات');
  }
  // If GENERAL or if filter returned nothing, return all questions
  if (filteredQuestions.length === 0 && topic !== QuizTopic.FINES) {
      filteredQuestions = PDF_QUESTIONS;
  }
  
  return filteredQuestions.map((q, index) => ({
    ...q,
    id: `pdf-q-${index}`,
    isLoadingImage: false
  }));
};

